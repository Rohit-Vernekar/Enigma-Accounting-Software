    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        if(jTextField1.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null,"Agency Name cannot be Empty","Access Denied",JOptionPane.WARNING_MESSAGE);
            jTextField1.requestFocus();
        }            
        else{
            try{
                Connection conn=MySqlConnect.ConnectDB();
                String sql="SELECT * FROM AGENCY WHERE ANAME=?;";
                PreparedStatement ps=conn.prepareStatement(sql);
                ps.setString(1, jTextField1.getText());
                ResultSet rs=ps.executeQuery();
                if(rs.next()){
                    if(!rs.getString("aname").equalsIgnoreCase(aname)){
                        JOptionPane.showMessageDialog(null,"Agency Already Exists!","Access Denied",JOptionPane.WARNING_MESSAGE);
                        jTextField1.requestFocus();
                        jTextField1.selectAll();
                    }
                    else{
                        jTextField2.requestFocus();
                        jTextField2.selectAll();
                    }
                }
                else{
                    jTextField2.requestFocus();
                    jTextField2.selectAll();
                }
            }
            catch(SQLException | HeadlessException e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

