    private void addAgency(){
        if(flag==0){
            String sql="INSERT INTO AGENCY VALUES(?,?,?,?,?,?,?,?);";
            Connection conn=MySqlConnect.ConnectDB();
            try{
                PreparedStatement ps=conn.prepareStatement(sql);
                ps.setString(1, jTextField1.getText());
                ps.setString(2, jTextField2.getText());
                ps.setString(6, jTextField3.getText());
                ps.setString(7, jTextField4.getText());
                ps.setString(3, jTextField5.getText());
                ps.setString(8, jTextField6.getText());
                ps.setString(4, jTextField7.getText());
                ps.setDouble(5, 0.0);
                ps.executeUpdate();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        else{
            String sql="UPDATE AGENCY SET ANAME=?,ADDRESS=?,PHONE=?,GSTIN=?,ADDRESS2=?,ADDRESS3=?,PHONE2=? WHERE ANAME=?;";
            Connection conn=MySqlConnect.ConnectDB();
            try{
                PreparedStatement ps=conn.prepareStatement(sql);
                ps.setString(1, jTextField1.getText());
                ps.setString(2, jTextField2.getText());
                ps.setString(5, jTextField3.getText());
                ps.setString(6, jTextField4.getText());
                ps.setString(3, jTextField5.getText());
                ps.setString(7, jTextField6.getText());
                ps.setString(4, jTextField7.getText());
                ps.setString(8, jTextField1.getText());
                ps.executeUpdate();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
