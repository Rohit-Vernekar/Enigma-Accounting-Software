    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        if(jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "This Field cannot be Empty!");
            jTextField1.requestFocus();
            jTextField1.selectAll();
        }
        else{
            Connection conn=MySqlConnect.ConnectDB();
            String sql="SELECT * FROM COMPANY WHERE CNAME='" +jTextField1.getText() +"';";
            try{
                Statement s=conn.createStatement();
                ResultSet rs=s.executeQuery(sql);
                if(rs.next()){
                    if(flag==0){
                        JOptionPane.showMessageDialog(null, "Company Already Exists!");
                        jTextField1.requestFocus();
                        jTextField1.selectAll();
                    }
                    else{
                        if(!jTextField1.getText().equalsIgnoreCase(company)){
                            JOptionPane.showMessageDialog(null, "Company Already Exists!");
                            jTextField1.requestFocus();
                            jTextField1.selectAll();
                        }
                        else{
                            String ObjButtons[] = {"Yes","No"};
                            int PromptResult = JOptionPane.showOptionDialog(null,"Modify Company?","SAVE?",JOptionPane.DEFAULT_OPTION,JOptionPane.WARNING_MESSAGE,null,ObjButtons,ObjButtons[1]);
                            if(PromptResult==JOptionPane.YES_OPTION){
                                String sql2="UPDATE COMPANY SET CNAME=? WHERE CNAME=?;";
                                PreparedStatement ps=conn.prepareCall(sql2);
                                ps.setString(1, jTextField1.getText());
                                ps.setString(2, company);
                                ps.executeUpdate();
                                this.dispose();
                            }
                            else{
                                jTextField1.requestFocus();
                                jTextField1.selectAll();
                            }
                        }
                    }
                }
                else{
                    if(flag==0){
                        String ObjButtons[] = {"Yes","No"};
                        int PromptResult = JOptionPane.showOptionDialog(null,"Add Company?","SAVE?",JOptionPane.DEFAULT_OPTION,JOptionPane.WARNING_MESSAGE,null,ObjButtons,ObjButtons[1]);
                        if(PromptResult==JOptionPane.YES_OPTION){
                            String sql2="INSERT INTO COMPANY VALUES(?);";
                            PreparedStatement ps=conn.prepareCall(sql2);
                            ps.setString(1, jTextField1.getText());
                            ps.executeUpdate();
                            this.dispose();
                        }
                        else{
                            jTextField1.requestFocus();
                            jTextField1.selectAll();
                        }
                    }
                    else{
                        String ObjButtons[] = {"Yes","No"};
                        int PromptResult = JOptionPane.showOptionDialog(null,"Modify Company?","SAVE?",JOptionPane.DEFAULT_OPTION,JOptionPane.WARNING_MESSAGE,null,ObjButtons,ObjButtons[1]);
                        if(PromptResult==JOptionPane.YES_OPTION){
                            String sql2="UPDATE COMPANY SET CNAME=? WHERE CNAME=?;";
                            PreparedStatement ps=conn.prepareCall(sql2);
                            ps.setString(1, jTextField1.getText());
                            ps.setString(2, company);
                            ps.executeUpdate();
                            this.dispose();
                        }
                        else{
                            jTextField1.requestFocus();
                            jTextField1.selectAll();
                        }
                    }
                }
            }
            catch(SQLException | HeadlessException e){
                JOptionPane.showMessageDialog(null, e);
            }
            
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

    private void jTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyPressed
        if(evt.getKeyCode()==KeyEvent.VK_ESCAPE){
            if(jTextField1.getText().isEmpty()){
                Company_Page.this.dispose();
            }
            else{
                String ObjButtons[] = {"Yes","No"};
                int PromptResult = JOptionPane.showOptionDialog(null,"Exit Without Saving Changes?","EXIT?",JOptionPane.DEFAULT_OPTION,JOptionPane.WARNING_MESSAGE,null,ObjButtons,ObjButtons[1]);
                if(PromptResult==JOptionPane.YES_OPTION){
                    this.dispose();
                }
                else{
                    jTextField1.requestFocus();
                    jTextField1.selectAll();
                }
            }
        }
    }//GEN-LAST:event_jTextField1KeyPressed

